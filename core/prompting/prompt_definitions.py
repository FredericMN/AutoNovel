# =============== 全局 SYSTEM 提示词 ===============
import logging

def resolve_global_system_prompt(enabled: bool = None) -> str:
    """
    根据开关决定是否加载全局 system prompt。

    从 PromptManager 读取全局提示词内容，统一管理所有提示词配置。

    Args:
        enabled: 兼容旧版本的布尔参数，如果为None则从PromptManager读取配置

    Returns:
        全局system prompt内容，如果未启用或内容为空则返回空字符串
    """
    from .prompt_manager import PromptManager
    pm = PromptManager()

    # 优先使用PromptManager的配置（新版本）
    if enabled is None:
        enabled = pm.is_module_enabled("helper", "global_system")

    if not enabled:
        return ""

    # 从 PromptManager 读取全局提示词内容
    prompt = pm.get_prompt("helper", "global_system")

    if not prompt:
        logging.warning("启用全局 system prompt 但未找到有效内容，将跳过注入。")
        return ""

    logging.info("已加载全局 system prompt，长度: %d 字符", len(prompt))
    return prompt


